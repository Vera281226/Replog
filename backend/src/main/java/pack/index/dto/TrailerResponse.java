package pack.index.dto;

import lombok.Getter;
import lombok.Setter;

/**
 * TrailerResponse 클래스
 * -------------------------------------------------------------------
 * ○ 프론트 index.html에서 '추천 예고편' 섹션에 사용하는 응답 DTO
 * ○ 콘텐츠 ID, 제목, 설명, 포스터, 유튜브 영상 키 포함
 * -------------------------------------------------------------------
 */
@Getter
@Setter
public class TrailerResponse {

    // -------------------------------------------------------------
    // 콘텐츠 고유 ID
    // - 내부 DB 기준 content_id
    // - 예: 105
    // -------------------------------------------------------------
    private Long contentId;

    // -------------------------------------------------------------
    // 콘텐츠 제목
    // - 예: "범죄도시 4"
    // -------------------------------------------------------------
    private String title;

    // -------------------------------------------------------------
    // 콘텐츠 줄거리 (설명)
    // - 예: "괴물형사 VS 괴물 CEO"
    // -------------------------------------------------------------
    private String overview;

    // -------------------------------------------------------------
    // 포스터 이미지 경로
    // - TMDB 상대 경로 (예: /abc123.jpg)
    // -------------------------------------------------------------
    private String posterPath;

    // -------------------------------------------------------------
    // 예고편 유튜브 영상 키
    // - 유튜브 URL 형식: https://www.youtube.com/watch?v={key}
    // - 예: "x1234abcd"
    // -------------------------------------------------------------
    private String youtubeKey;
}
