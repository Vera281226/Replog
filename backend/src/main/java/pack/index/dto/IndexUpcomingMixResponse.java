package pack.index.dto;

import lombok.Getter;
import lombok.Setter;

/**
 * IndexUpcomingMixResponse 클래스
 * -------------------------------------------------------------------
 * ○ '콘텐츠 예정작' 섹션에 사용되는 응답 DTO
 * ○ TMDB에서 가져온 넷플릭스/디즈니+ 예정 콘텐츠 정보를 포함
 * -------------------------------------------------------------------
 */
@Getter
@Setter
public class IndexUpcomingMixResponse {

    // -------------------------------------------------------------
    // 콘텐츠 고유 ID
    // - TMDB에서 받은 외부 콘텐츠 ID
    // - 예: 123456
    // -------------------------------------------------------------
    private int contentId;

    // -------------------------------------------------------------
    // 콘텐츠 제목
    // - 예: "어벤져스 5"
    // -------------------------------------------------------------
    private String title;

    // -------------------------------------------------------------
    // 포스터 이미지 경로
    // - TMDB 상대 경로 (예: /poster.jpg)
    // -------------------------------------------------------------
    private String posterPath;

    // -------------------------------------------------------------
    // 개봉일
    // - 형식: yyyy-MM-dd
    // - 예: "2025-09-01"
    // -------------------------------------------------------------
    private String releaseDate;

    // -------------------------------------------------------------
    // 제공 플랫폼
    // - 예: "netflix", "disney"
    // -------------------------------------------------------------
    private String platform;

    // -------------------------------------------------------------
    // 미디어 타입
    // - 예: "movie", "tv"
    // - 영화/TV 구분 태그 표시 가능
    // -------------------------------------------------------------
    private String mediaType;

    // -------------------------------------------------------------
    // 백드롭 이미지 경로
    // - TMDB 가로형 배경 이미지 (예: /backdrop.jpg)
    // - 선택 사항 (슬라이더, hover 배경 등에서 활용 가능)
    // -------------------------------------------------------------
    private String backdropPath;
}
