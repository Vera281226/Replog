package pack.index.dto;

import lombok.Getter;
import lombok.Setter;

/**
 * UpcomingResponse 클래스
 * -------------------------------------------------------------------
 * ○ 프론트 index.html에서 '개봉 예정 영화' 섹션에 사용하는 응답 DTO
 * ○ 콘텐츠 ID, 제목, 포스터 경로, 개봉일, 평점 정보 포함
 * -------------------------------------------------------------------
 */
@Getter
@Setter
public class UpcomingResponse {

    // -------------------------------------------------------------
    // 콘텐츠 고유 ID
    // - 내부 DB 기준 content_id
    // - 예: 101
    // -------------------------------------------------------------
    private Long contentId;

    // -------------------------------------------------------------
    // 콘텐츠 제목
    // - 예: "듄: 파트2"
    // -------------------------------------------------------------
    private String title;

    // -------------------------------------------------------------
    // 포스터 이미지 경로
    // - TMDB 상대 경로 (예: /abc123.jpg)
    // -------------------------------------------------------------
    private String posterPath;

    // -------------------------------------------------------------
    // 개봉일 (YYYY-MM-DD)
    // - 예: 2025-08-15
    // - release_date 필드
    // -------------------------------------------------------------
    private String releaseDate;

    // -------------------------------------------------------------
    // 평균 평점 (0.0 ~ 10.0)
    // - TMDB 기준 평점
    // - 예: 7.8
    // -------------------------------------------------------------
    private Float rating;
}
